public class Data
{
	double arrivalTime;
	double departureTime;

	public Data()
	{

	}

	public void setArrivalTime(double a)
	{
		this.arrivalTime = a;
	}

	public void setDepartureTime(double d)
	{
		this.departureTime = d;
	}

	public double getDepartureTime()
	{
		return departureTime;
	}

	public double getArrivalTime()
	{
		return arrivalTime;
	}
}